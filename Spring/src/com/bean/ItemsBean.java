package com.bean;

public class ItemsBean {
	
	private String itemname;
	private Float itemprice;
	private String itemvendor;
	private int itemoffer;
	private String itemcategory;
	private int itemquantity;
	private String imagename;
	private String itemid;
	public String getItemname() {
		return itemname;
	}
	public void setItemname(String itemname) {
		this.itemname = itemname;
	}
	public Float getItemprice() {
		return itemprice;
	}
	public void setItemprice(Float itemprice) {
		this.itemprice = itemprice;
	}
	public String getItemvendor() {
		return itemvendor;
	}
	public void setItemvendor(String itemvendor) {
		this.itemvendor = itemvendor;
	}
	public int getItemoffer() {
		return itemoffer;
	}
	public void setItemoffer(int itemoffer) {
		this.itemoffer = itemoffer;
	}
	public String getItemcategory() {
		return itemcategory;
	}
	public void setItemcategory(String itemcategory) {
		this.itemcategory = itemcategory;
	}
	public int getItemquantity() {
		return itemquantity;
	}
	public void setItemquantity(int itemquantity) {
		this.itemquantity = itemquantity;
	}
	public String getImagename() {
		return imagename;
	}
	public void setImagename(String imagename) {
		this.imagename = imagename;
	}
	public String getItemid() {
		return itemid;
	}
	public void setItemid(String itemid) {
		this.itemid = itemid;
	}
	
	
	
}
